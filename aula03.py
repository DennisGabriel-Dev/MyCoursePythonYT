b = float(input ("Digite um número real: "))#Essa variável recebe um número real 

#A condição "if" é basicamente o "se" em português. Bom, a explicação de como ele funciona é o seguinte:
#Ele , o "if", só é executado se a condição entre parênteses após ele for verdadeira.
if(b>100):
    print("B é maior que 100")

#A condição else só é executada se o if apresentado anteriormente tiver a condição falsa. 
else:
    print("B é menor que 100") 

